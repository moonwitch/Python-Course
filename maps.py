# EXERCISE 1: Square root of map
###############################################################################
def mapper(args):
    return args**2


print(list(map(mapper, [1, 2, 3, 4, 5])))


###############################################################################
# EXERCISE 2: convert to F of map
###############################################################################
def convert_to_f(args):
    return (args * 9 / 5) + 32


print(list(map(convert_to_f, [0, 22, 5, -10])))


###############################################################################
# EXERCISE 3: filter 0 from list
###############################################################################
def filter_out(args):
    return args != 0


print(list(filter(filter_out, [0, 1, 2, 3, 0, 4, 5])))
