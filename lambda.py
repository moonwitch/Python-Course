# Lambda:
# lambda arguments: expression
###############################################################################
# EXERCISE 1: lambda square root to list with map
###############################################################################
print(list(map(lambda args: args**2, [1, 2, 3, 4, 5])))
###############################################################################
# EXERCISE 2: filter larger than 2
###############################################################################
print(list(filter(lambda args: args > 2, [1, 0, 3, 5, 9, 1, 2])))
###############################################################################
# EXERCISE 3: filter neg
###############################################################################
print(list(filter(lambda args: args >= 0, [-2, -4, 1, 0, -3, -7, -8, 5, 9, 1, -2])))
###############################################################################
# EXERCISE 4: filter uneven
###############################################################################
print(list(filter(lambda args: args % 2 != 0, [1, 0, 3, 5, 9, 1, 2])))
